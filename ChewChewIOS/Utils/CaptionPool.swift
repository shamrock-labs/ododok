import Foundation

/// 리포트·알림·UI 문구 풀. 카테고리별 배열에서 랜덤 선택해 재사용. 각 문장은 32자 이하.
/// REQ-03(리포트 점수 캡션)·REQ-13(끼니 알림) 등에서 공용으로 사용한다.
enum CaptionPool {

    enum Category {
        case report
        // 향후: streak, challenge, etc.
    }

    /// `grade`에 해당하는 캡션 풀에서 한 문장을 반환. 풀이 비어 있으면 nil.
    static func report(for grade: ReportCardModel.Grade) -> String? {
        let pool: [String]
        switch grade {
        case .good:
            pool = [
                "오늘은 정말 잘 씹었어요. 이 페이스 그대로 가요.",
                "꼼꼼히 씹은 한 끼였어요.",
                "꼭꼭 잘 씹었어요. 내일도 이 페이스로.",
                "식사 리듬이 안정적이에요. 좋은 흐름이에요.",
            ]
        case .soso:
            pool = [
                "조금만 더 천천히 먹으면 좋겠어요.",
                "한 입에 30회만 신경 써도 점수가 달라져요.",
                "다음 식사엔 속도를 살짝만 줄여봐요.",
                "꾸준히 하다 보면 금방 좋아져요. 오늘도 수고했어요.",
            ]
        case .bad:
            pool = [
                "이번엔 조금 빨리 먹었네요. 다음엔 한 입 30회를 목표로 해봐요.",
                "천천히 씹으면 포만감이 오래가요. 다음 끼니에 다시.",
                "속도를 조금만 줄여도 점수가 달라져요.",
                "괜찮아요. 다음 한 끼부터 천천히 가봐요.",
            ]
        }
        return pool.randomElement()
    }

    /// 끼니 알림 본문 풀(다람이 어투). 서버 푸시·로컬 알림이 같은 카피를 쓰도록 양쪽을 일치시킨다.
    static let mealReminder: [String] = [
        "정해진 식사 시간이 다가오고 있어요. 오늘 첫 3분은 평소보다 조금만 더 천천히 드셔보시는 건 어떨까요?",
        "곧 식사 시간이에요. 다람이와 함께 꼭꼭 씹으며 천천히 시작해볼까요?",
        "밥 먹을 시간이 다가와요. 오늘도 한 입 한 입 정성껏 씹어봐요.",
        "식사 시간이 되었어요. 천천히 드시면 포만감이 더 오래간대요.",
        "슬슬 식사할 시간이에요. 측정도 같이 시작하면 도토리가 쌓여요.",
        "오늘 한 끼도 건강하게 챙겨요. 다람이가 옆에서 응원할게요.",
        "식사 시간 잊지 않으셨죠? 한 입씩 꼭꼭 씹는 것부터 시작해봐요.",
        "맛있는 한 끼 드실 시간이에요. 천천히, 다람이랑 같이 기록해요.",
    ]

    /// 배열에서 랜덤 문자열 반환. 배열이 비어 있으면 빈 문자열.
    static func random(from pool: [String]) -> String {
        pool.randomElement() ?? ""
    }
}
